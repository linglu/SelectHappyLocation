public class RoomInfo {
    private String room_id = null;
    private String room_number = null;
    private String room_flag = null;
    private String user_id = null;
    private String room_time = null;
    private String room_full = null;
    private String room_alias = null;
    private String room_description = null;

    public String getRoom_id() {
        return room_id;
    }

    public void setRoom_id(String room_id) {
        this.room_id = room_id;
    }

    public String getRoom_number() {
        return room_number;
    }

    public void setRoom_number(String room_number) {
        this.room_number = room_number;
    }

    public String getRoom_flag() {
        return room_flag;
    }

    public void setRoom_flag(String room_flag) {
        this.room_flag = room_flag;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getRoom_time() {
        return room_time;
    }

    public void setRoom_time(String room_time) {
        this.room_time = room_time;
    }

    public String getRoom_full() {
        return room_full;
    }

    public void setRoom_full(String room_full) {
        this.room_full = room_full;
    }

    public String getRoom_alias() {
        return room_alias;
    }

    public void setRoom_alias(String room_alias) {
        this.room_alias = room_alias;
    }

    public String getRoom_description() {
        return room_description;
    }

    public void setRoom_description(String room_description) {
        this.room_description = room_description;
    }
}
